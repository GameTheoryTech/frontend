import { BigNumber, Contract } from 'ethers';
import { Signer } from '@ethersproject/abstract-signer';
import { Provider } from '@ethersproject/abstract-provider';
import {TransactionReceipt, TransactionResponse} from '@ethersproject/providers';
import { formatUnits } from 'ethers/lib/utils';
import ERC20 from "./ERC20";

class ERC20Lockable extends ERC20 {

  constructor(address: string, provider: Signer | Provider, symbol: string, decimal = 18) {
    super(address, provider, symbol, decimal, LockableABI);
  }

  connect(signerOrProvider: Signer | Provider) {
    this.contract = new Contract(this.address, LockableABI, signerOrProvider);
  }

  lockOf(account: string): Promise<BigNumber> {
    return this.contract.lockOf(account);
  }

  canUnlockAmount(account: string): Promise<BigNumber> {
    return this.contract.canUnlockAmount(account);
  }

  totalBalanceOf(account: string): Promise<BigNumber> {
    return this.contract.totalBalanceOf(account);
  }

  noUnlockBeforeTransfer(account: string): Promise<boolean> {
    return this.contract.noUnlockBeforeTransfer(account);
  }

  setNoUnlockBeforeTransfer(set : boolean): Promise<TransactionResponse> {
    return this.contract.setNoUnlockBeforeTransfer(set);
  }
}

